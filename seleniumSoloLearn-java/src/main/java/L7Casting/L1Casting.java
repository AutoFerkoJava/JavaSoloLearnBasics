package L7Casting;
/*
 Assigning a value of one type to a variable of another type is known 
 as Type Casting.

To cast a value to a specific type, place the type in parentheses 
and position it in front of the value.
Example:
 */
public class L1Casting {
	public static void main(String[] args) {
        int a = (int) 3.14;
        System.out.println(a);
    }
}