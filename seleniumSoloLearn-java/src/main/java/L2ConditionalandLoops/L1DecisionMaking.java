/*
Conditional statements are used to perform different actions based on different conditions.
The if statement is one of the most frequently used conditional statements.
If the if statement's condition expression evaluates to true, 
the block of code inside the if statement is executed. 
If the expression is found to be false, the first set of code 
after the end of the if statement (after the closing curly brace) is executed.

Syntax:*/

/*
if (condition) {
//Executes when the condition is true
}*/

